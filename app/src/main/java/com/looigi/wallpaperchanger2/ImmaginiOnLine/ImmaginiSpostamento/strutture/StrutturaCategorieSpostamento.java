package com.looigi.wallpaperchanger2.ImmaginiOnLine.ImmaginiSpostamento.strutture;

public class StrutturaCategorieSpostamento {
    private int idCategoria;
    private String Categoria;
    private int ImmaginiContenute;

    public int getIdCategoria() {
        return idCategoria;
    }

    public void setIdCategoria(int idCategoria) {
        this.idCategoria = idCategoria;
    }

    public String getCategoria() {
        return Categoria;
    }

    public void setCategoria(String categoria) {
        Categoria = categoria;
    }

    public int getImmaginiContenute() {
        return ImmaginiContenute;
    }

    public void setImmaginiContenute(int immaginiContenute) {
        ImmaginiContenute = immaginiContenute;
    }
}
