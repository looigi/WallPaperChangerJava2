package com.looigi.wallpaperchanger2.classeModifiche.Strutture;

public class Modifiche {
    int idProgetto;
    int idModulo;
    int idSezione;
    int idModifica;
    String Modifica;
    int idStato;

    public int getIdModifica() {
        return idModifica;
    }

    public void setIdModifica(int idModifica) {
        this.idModifica = idModifica;
    }

    public int getIdModulo() {
        return idModulo;
    }

    public void setIdModulo(int idModulo) {
        this.idModulo = idModulo;
    }

    public int getIdProgetto() {
        return idProgetto;
    }

    public void setIdProgetto(int idProgetto) {
        this.idProgetto = idProgetto;
    }

    public int getIdSezione() {
        return idSezione;
    }

    public void setIdSezione(int idSezione) {
        this.idSezione = idSezione;
    }

    public int getIdStato() {
        return idStato;
    }

    public void setIdStato(int idStato) {
        this.idStato = idStato;
    }

    public String getModifica() {
        return Modifica;
    }

    public void setModifica(String modifica) {
        Modifica = modifica;
    }
}
