package com.looigi.wallpaperchanger2.Onomastici.strutture;

public class DatiColori {
	private int Trasparenza;
	private int Rosso;
	private int Verde;
	private int Blu;
	
	public int getTrasparenza() {
		return Trasparenza;
	}
	public void setTrasparenza(int trasparenza) {
		Trasparenza = trasparenza;
	}
	public int getRosso() {
		return Rosso;
	}
	public void setRosso(int rosso) {
		Rosso = rosso;
	}
	public int getVerde() {
		return Verde;
	}
	public void setVerde(int verde) {
		Verde = verde;
	}
	public int getBlu() {
		return Blu;
	}
	public void setBlu(int blu) {
		Blu = blu;
	}

	private int TrasparenzaT;
	private int RossoT;
	private int VerdeT;
	private int BluT;
	
	public int getTrasparenzaT() {
		return TrasparenzaT;
	}
	public void setTrasparenzaT(int trasparenzaT) {
		TrasparenzaT = trasparenzaT;
	}
	public int getRossoT() {
		return RossoT;
	}
	public void setRossoT(int rossoT) {
		RossoT = rossoT;
	}
	public int getVerdeT() {
		return VerdeT;
	}
	public void setVerdeT(int verdeT) {
		VerdeT = verdeT;
	}
	public int getBluT() {
		return BluT;
	}
	public void setBluT(int bluT) {
		BluT = bluT;
	}
}
