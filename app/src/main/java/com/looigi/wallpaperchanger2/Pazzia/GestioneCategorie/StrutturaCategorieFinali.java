package com.looigi.wallpaperchanger2.Pazzia.GestioneCategorie;

public class StrutturaCategorieFinali {
    private String CategoriaImm;
    private String CategoriaVid;
    private String CategoriaOriginaleImm;
    private String CategoriaOriginaleVid;

    public String getCategoriaImm() {
        return CategoriaImm;
    }

    public void setCategoriaImm(String categoriaImm) {
        CategoriaImm = categoriaImm;
    }

    public String getCategoriaVid() {
        return CategoriaVid;
    }

    public void setCategoriaVid(String categoriaVid) {
        CategoriaVid = categoriaVid;
    }

    public String getCategoriaOriginaleImm() {
        return CategoriaOriginaleImm;
    }

    public void setCategoriaOriginaleImm(String categoriaOriginaleImm) {
        CategoriaOriginaleImm = categoriaOriginaleImm;
    }

    public String getCategoriaOriginaleVid() {
        return CategoriaOriginaleVid;
    }

    public void setCategoriaOriginaleVid(String categoriaOriginaleVid) {
        CategoriaOriginaleVid = categoriaOriginaleVid;
    }
}
