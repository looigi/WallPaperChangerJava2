package com.looigi.wallpaperchanger2.Lazio.Strutture;

public class StrutturaAllenatori {
    private int idAllenatore;
    private String Cognome;
    private String Nome;

    public int getIdAllenatore() {
        return idAllenatore;
    }

    public void setIdAllenatore(int idAllenatore) {
        this.idAllenatore = idAllenatore;
    }

    public String getCognome() {
        return Cognome;
    }

    public void setCognome(String cognome) {
        Cognome = cognome;
    }

    public String getNome() {
        return Nome;
    }

    public void setNome(String nome) {
        Nome = nome;
    }
}
