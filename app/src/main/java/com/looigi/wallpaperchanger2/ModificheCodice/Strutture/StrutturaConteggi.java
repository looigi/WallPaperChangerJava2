package com.looigi.wallpaperchanger2.ModificheCodice.Strutture;

public class StrutturaConteggi {
    private int idProgetto;
    private int idModulo;
    private int idSezione;
    private String Progetto;
    private String Modulo;
    private String Sezione;
    private int Quante;

    public int getIdProgetto() {
        return idProgetto;
    }

    public void setIdProgetto(int idProgetto) {
        this.idProgetto = idProgetto;
    }

    public int getIdModulo() {
        return idModulo;
    }

    public void setIdModulo(int idModulo) {
        this.idModulo = idModulo;
    }

    public int getIdSezione() {
        return idSezione;
    }

    public void setIdSezione(int idSezione) {
        this.idSezione = idSezione;
    }

    public String getProgetto() {
        return Progetto;
    }

    public void setProgetto(String progetto) {
        Progetto = progetto;
    }

    public String getModulo() {
        return Modulo;
    }

    public void setModulo(String modulo) {
        Modulo = modulo;
    }

    public String getSezione() {
        return Sezione;
    }

    public void setSezione(String sezione) {
        Sezione = sezione;
    }

    public int getQuante() {
        return Quante;
    }

    public void setQuante(int quante) {
        Quante = quante;
    }
}
