package com.looigi.wallpaperchanger2.classeOrari.strutture;

public class StrutturaTempo {
    private int idTempo;
    private String Tempo;
    private String urlIcona;

    public String getUrlIcona() {
        return urlIcona;
    }

    public void setUrlIcona(String urlIcona) {
        this.urlIcona = urlIcona;
    }

    public int getIdTempo() {
        return idTempo;
    }

    public void setIdTempo(int idTempo) {
        this.idTempo = idTempo;
    }

    public String getTempo() {
        return Tempo;
    }

    public void setTempo(String tempo) {
        Tempo = tempo;
    }
}
