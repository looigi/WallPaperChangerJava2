package com.looigi.wallpaperchanger2.classeUtilityImmagini.strutture;

public class StrutturaUgualiInterna {
    private String Filtro;
    private int Quanti;
    private String Tipo;

    public String getTipo() {
        return Tipo;
    }

    public void setTipo(String tipo) {
        Tipo = tipo;
    }

    public String getFiltro() {
        return Filtro;
    }

    public void setFiltro(String filtro) {
        Filtro = filtro;
    }

    public int getQuanti() {
        return Quanti;
    }

    public void setQuanti(int quanti) {
        Quanti = quanti;
    }
}
